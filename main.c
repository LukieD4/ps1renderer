// Concatenated texture blob (all TIMs back-to-back) + its offset/length
// table, both generated by py_convert_textures.py. tex_blob_start is the
// base pointer every offset in texBlobMaterialTable is relative to - this
// replaces the old single-texture face_tim_start[] + tim_face.S pair.
//
// TEX_BLOB_ENTRY (and the tex_blob_start/end + texBlobMaterialTable
// externs) now live in generated/tex_blob_table.h, not here - it used to
// be a hand-copied typedef in main.c that silently drifted out of sync
// with the generator's struct (missing paletteCount). One shared header
// now; see py_convert_textures.py's build_blob() for the generator side.
#include "generated/tex_blob_table.h"

// Runtime texture table - one resolved TIM_IMAGE per material name found
// in the blob. Built once in load_textures() at startup by walking
// texBlobMaterialTable and calling GetTimInfo() on each blob slice.
// Sized MAX_TEXTURES rather than texBlobEntryCount so it's a fixed-size
// array (simpler than a VLA/malloc on PS1); bump MAX_TEXTURES if this
// ever gets hit - MODEL_TRI.material is unsigned char so the real ceiling
// is 256, this is just today's practical cap.
#define MAX_TEXTURES 32

typedef struct
{
    const char *material;
    TIM_IMAGE tim;
    uint8_t loaded;

    // Number of stacked CLUT rows this texture actually has (1 if a
    // plain single-palette texture). Copied from the blob entry in
    // load_textures(); draw_model() clamps active_palette against this
    // per-texture before adding it to tim.crect->y, so a texture with
    // fewer rows than currently selected can't read past its own CLUT
    // block into another texture's VRAM data.
    unsigned int paletteCount;
} MODEL_TEXTURE;

static MODEL_TEXTURE textureTable[MAX_TEXTURES];
static unsigned int textureTableCount = 0;

// Per-model material name table (modelMaterialNames[]/modelMaterialCount),
// emitted by py_convert_assets.py from the .obj's usemtl lines, is
// #include'd above as part of generated/house.c. tri->material is an
// index into THAT array (per-model, first-seen order in the .obj) -
// draw_model() resolves modelMaterialNames[tri->material] (a name) against
// textureTable[] (also keyed by name) to find the actual TIM_IMAGE, if any.
// This indirection is what lets material index 0 mean different textures
// in different models safely.




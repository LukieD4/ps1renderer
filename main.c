// ------------------------------------------------------------
// GPU Ordering Table
// ------------------------------------------------------------
// Number of depth buckets in the OT. Each bucket holds a linked list of
// primitives at roughly that depth; DrawOTag() walks every bucket
// farthest-to-nearest in hardware - this is what sorts the frame (see
// draw_object_into_ot()). More buckets = finer back-to-front resolution
// at the cost of a bigger ClearOTagR() per frame and more static RAM;
// bump if far-apart triangles start visibly fighting.
#define OT_LEN 1024

// Right-shift applied to the raw gte_avsz3()/gte_stotz() depth value
// before clamping it into an OT bucket index. Per inline_c.h, gte_avsz3()
// computes OTZ = ZSF3*(SZ1+SZ2+SZ3)/4096 - ZSF3 itself is set by
// InitGeom() (not exposed as a header macro, so its default isn't
// visible here), so the exact numeric range OTZ lands in for this
// project's camera/model scale is still empirical. TUNE THIS: raise the
// shift if distant geometry clumps into the last bucket; lower it if
// nearby triangles land in too few buckets.
#define OT_Z_SHIFT 2

// Upper bound on triangles insertable into the OT in a single frame,
// across every visible object. primBytesRemaining enforces this as a
// hard runtime cap - an over-budget stage drops triangles (reported via
// debug_text()'s PRIM_OVERFLOW counter) instead of overrunning the
// primitive arena.
#define MAX_PRIMS_PER_FRAME 4096



